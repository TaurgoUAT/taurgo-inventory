                name: "Electric Meter",
                selectedCondition: electricMeter,
                onConditionSelected: (condition) {
                  setState(() {
                    electricMeter = condition;
                  });
                },
              ),

              //Water Meter
              ConditionItem(
                name: "Water Meter",
