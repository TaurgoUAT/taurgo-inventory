                onConditionSelected: (condition) {
                  setState(() {
                    waterMeter = condition;
                  });
                },
              ),

              //Oil Meter
              ConditionItem(
                name: "Oil Meter",
                selectedCondition: oilMeter,
                onConditionSelected: (condition) {
                  setState(() {
                    oilMeter = condition;
                  });
                },
              ),


              // Add more ConditionItem widgets as needed
            ],
